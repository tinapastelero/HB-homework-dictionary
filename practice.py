def get_sum_zero_pairs(numbers):
    """Given list of numbers, return list of pair summing to 0.

    Given a list of numbers, add up each individual pair of numbers.
    Return a list of each pair of numbers that adds up to 0.

    For example::

        >>> sort_pairs( get_sum_zero_pairs([1, 2, 3, -2, -1]) )
        [[-2, 2], [-1, 1]]

        >>> sort_pairs( get_sum_zero_pairs([3, -3, 2, 1, -2, -1]) )
        [[-3, 3], [-2, 2], [-1, 1]]

    This should always be a unique list, even if there are
    duplicates in the input list::

        >>> sort_pairs( get_sum_zero_pairs([1, 2, 3, -2, -1, 1, 1]) )
        [[-2, 2], [-1, 1]]

    Of course, if there are one or more zeros to pair together,
    that's fine, too (even a single zero can pair with itself)::

        >>> sort_pairs( get_sum_zero_pairs([1, 3, -1, 1, 1, 0]) )
        [[-1, 1], [0, 0]]
    """
    my_dictionary = {}

    for i in range(len(numbers)):
        if -(numbers[i]) in numbers:
            my_tuple = tuple(sorted((-(numbers[i]), (numbers[i]))))
            my_dictionary[my_tuple] = my_dictionary.get(my_tuple,0)
    if 0 in numbers:
        my_dictionary[(0, 0)] = my_dictionary.get((0,0), 0)

    return my_dictionary



def top_chars(phrase):
    """Find most common character(s) in string.

    Given an input string, return a list of character(s) which
    appear(s) the most the input string.

    If there is a tie, the order of the characters in the returned
    list should be alphabetical.

    For example::

        >>> top_chars("The rain in spain stays mainly in the plain.")
        ['i', 'n']

    If there is not a tie, simply return a list with one item.

    For example::

        >>> top_chars("Shake it off, shake it off.")
        ['f']

    Do not count spaces, but count all other characters.

    """

    my_dict = {}

    for character in phrase:
        my_dict[character] = my_dict.get(character, 0) + 1

    del my_dict[" "]

    #create list of keys and list of values
    keys_list = my_dict.keys()
    values_list = my_dict.values()

    #find index for max count of characters
    keys_list_index = []
    for i in range(len(values_list)):
        if values_list[i] == max(values_list):
            keys_list_index.append(i)

    #find corresponding characters in list of keys
    final_characters =[]
    for i in keys_list_index:
        final_characters.append(keys_list[i])

    return sorted(final_characters)

